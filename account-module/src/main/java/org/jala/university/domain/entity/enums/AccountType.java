package org.jala.university.domain.entity.enums;

/**
 * Enumeration representing the types of bank accounts available in the system.
 * Used to categorize accounts and determine their specific features and behaviors.
 */
public enum AccountType {
    /**
     * Standard checking account
     * Allows for regular banking transactions with no interest earnings
     */
    CHECKING,

    /**
     * Savings account
     * Designed for saving money with interest earnings
     */
    SAVINGS
}
